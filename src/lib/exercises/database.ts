export const HABIT_DATABASE: HabitEntry[] = HABIT_TEMPLATES.flatMap((template) =>
  WEEK_VARIANTS.map((variant) => ({
    id: `${template.id}-w${variant.weekMin}`,
    title: `${template.title} - ${variant.label}`,
    description: `${template.description} Woche ${variant.weekMin} Fokus: saubere Ausfuehrung und regelmaessige Dokumentation.`,
    category: template.category,
    difficulty: getProgressedDifficulty(template.baseDifficulty, variant.difficultyDelta),
    durationMinutes: Math.max(1, Math.round(template.baseDuration * variant.durationFactor)),
    icon: template.icon,
    xp: template.baseXp + variant.xpBonus,
    evidenceLevel: template.evidenceLevel,
    tags: template.tags,
    gymLevel: template.gymLevel,
    weekMin: variant.weekMin,
  })),
);

export const getHabitsByTag = (tag: PersonalizationTag): HabitEntry[] => {
  return HABIT_DATABASE.filter((habit) => habit.tags.includes(tag));
};

export const getHabitById = (id: string): HabitEntry | undefined => {
  return HABIT_DATABASE.find((habit) => habit.id === id);
};
