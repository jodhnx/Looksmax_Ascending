const DIFFICULTY_ORDER: HabitEntry["difficulty"][] = ["leicht", "mittel", "schwer"];

const getProgressedDifficulty = (
  baseDifficulty: HabitEntry["difficulty"],
  delta: number,
): HabitEntry["difficulty"] => {
  const baseIndex = DIFFICULTY_ORDER.indexOf(baseDifficulty);
  const nextIndex = Math.min(DIFFICULTY_ORDER.length - 1, Math.max(0, baseIndex + delta));
  return DIFFICULTY_ORDER[nextIndex];
};

